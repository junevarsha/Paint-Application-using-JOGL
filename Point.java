public class Point {

	float pointX;
	float pointY;

	public Point(float px, float py) {
		this.pointX = px;
		this.pointY = py;
	}

	public float getPointX() {
		return pointX;
	}

	public void setPointX(float pointX) {
		this.pointX = pointX;
	}

	public float getPointY() {
		return pointY;
	}

	public void setPointY(float pointY) {
		this.pointY = pointY;
	}

}
